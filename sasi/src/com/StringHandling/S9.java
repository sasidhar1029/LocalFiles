package com.StringHandling;

public class S9 {

	public static void main(String[] args) {
		String s1 = "srikanth";
		String s2 = "java";
		System.out.println(s1);
		System.out.println(s1.concat(s2));
		int a = 10;
		int b = 20;
		String c = "java";
		String c1 = "sql";
		System.out.println(a + b + c + c1);
		System.out.println(c + c1 + a + b);
		System.out.println(c + c1 + a * b);
		System.out.println(c + c1 + a / b);
		System.out.println(c + c1 + a % b);
	}

}
