package com.oops;

public class Encapsulation2 {
	private int sid;
	private String name;
	private int age;

	public void setSid(int sid) {
		this.sid = sid;
	}

	public int getSid() {
		return sid;
	}

	public void setString(String name) {
		this.name = name;
	}

	public String getString() {
		return name;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public int getAge() {
		return age;
	}

}
